"""
Python - Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas

"""
print(1234)

# Aspas simples
print(1, 'Joseph Oliveira')
print('Joseph "Oliveira"')
# Aspas duplas
print("Joseph Oliveira")
print(2, "Joseph 'Oliveira'")

# Escape
print("Joseph \"Oliveira\"")

# r
print(r"Joseph \"Oliveira\"")