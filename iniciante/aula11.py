# IMC Exercício

# o índice de massa corporal de um adulto é o seu peso em quilos 
# (por exemplo, 80 kg), dividido por sua altura ao quadrado (vamos imaginar, 1,80 m x 1,80 m).

nome = "Joseph Oliveira"
peso = 105.00
altura = 1.87
imc = peso/(2**altura)
print(f'Nome: {nome}, peso: {peso} seu IMC é {imc}')